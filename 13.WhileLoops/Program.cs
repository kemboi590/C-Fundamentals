// while loops in c#
//checks the condition before execution

int index = 1;

while (index <= 5)
{
    Console.WriteLine("The index value is " +  index);
    index++;
}


//do while loop in c#
//executes first then checks condition

int val = 6;
do
{
    Console.WriteLine("The value is " + val);
    val++;
} while (val <5 );